#define BMP280_ADDR         (0x76 << 1)

#define BMP280_REG_TEMP_MSB 0xFA
#define BMP280_REG_CTRL_MEAS 0xF4
#define BMP280_REG_CALIB 0x88
#define BMP280_REG_TEMP_MSB 0xFA
