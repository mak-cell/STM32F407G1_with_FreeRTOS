// 2. A custom function to simulate heavy CPU load (Keeps Task in "Running" state)
void SimulateCpuWork(TickType_t ticksToWork) {
    TickType_t startTick = xTaskGetTickCount();

    // The CPU is trapped in this loop, running at 100% capacity
    // until the requested number of ticks has passed.
    while((xTaskGetTickCount() - startTick) < ticksToWork) {
        __NOP(); // Do nothing, just spin the CPU wheels
    }
}
